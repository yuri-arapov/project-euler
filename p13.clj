                        (let [n (+ shift (apply + i))]
                          ;; compute shift (overflow) and save one digit
                          [(quot n 10) (cons (rem n 10) res)]))

                      [0 '()]

                      ;; turn input into columns, rightmost column goes first
                      (reverse (transpose data)))]

    ;; get first 10 digits
    (subs (str s (apply str r)) 0 10)))

